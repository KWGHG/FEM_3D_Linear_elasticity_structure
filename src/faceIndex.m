function Ans = faceIndex(faceindex)
    
    if faceindex == 1
        Ans = [1 2 3 4];
    end

    if faceindex == 2
        Ans = [5 8 7 6];
    end

    if faceindex == 3
        Ans = [1 5 6 2];
    end

    if faceindex == 4
        Ans = [2 6 7 3];
    end

    if faceindex == 5
        Ans = [3 7 8 4];
    end

    if faceindex == 6
        Ans = [4 8 5 1];
    end
end